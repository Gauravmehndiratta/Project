<h1>ETYMOLOGY</h1>
<p class="two">
The origin of the word "travel" is most likely lost to history. The term "travel" may originate from the Old French word travail, which means 'work'.<br>
According to the Merriam Webster dictionary, the first known use of the word travel was in the 14th century.<br> 
It also states that the word comes from Middle English travailen, travelen (which means to torment, labor, strive, journey) and earlier from Old French travailler (which means to work strenuously, toil). <br>
In English we still occasionally use the words "travail", which means struggle. According to Simon Winchester in his book The Best Travelers' Tales (2004), the words "travel" and "travail" both share an even more ancient root: a Roman instrument of torture called the tripalium (in Latin it means "three stakes", as in to impale). This link may reflect the extreme difficulty of travel in ancient times. Today, travel may or may not be much easier depending upon the destination you choose (e.g. Mt. Everest, the Amazon rainforest), how you plan to get there (tour bus, cruise ship, or oxcart), and whether you decide to "rough it" (see extreme tourism and adventure travel). "There's a big difference between simply being a tourist and being a true world traveler", notes travel writer Michael Kasum. This is, however, a contested distinction as academic work on the cultures and sociology of travel has noted.
</p>
